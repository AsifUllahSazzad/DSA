def Selection_Sort(element):
    for i in range(len(element)-1):
        min_index = i
        for j in range(min_index+1,len(element)):
            if element[min_index]['First Name']>element[j]['First Name']:
                element[min_index],element[j] = element[j],element[min_index]
    return element
